package com.basics;

//Pgm to print hello 5 times and numbers 1 to 100 using while loop
public class A6 {
	public static void main(String[] args) {
		int i = 1;
		while (i <= 10) {
			System.out.println("hello");
			i++;
		}
		System.out.println("************************************");
		int j = 1;
		while (j <= 100) {
			System.out.println(j);
			j++;
		}
	}
}
